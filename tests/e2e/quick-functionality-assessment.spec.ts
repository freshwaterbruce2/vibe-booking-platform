import { test, expect, Page } from '@playwright/test';

test.describe('Quick Hotel Booking Functionality Assessment', () => {
  test.beforeEach(async ({ page }) => {
    // Go to the application
    await page.goto('http://localhost:3013');
    await page.waitForLoadState('networkidle');
  });

  test('Comprehensive Platform Assessment', async ({ page }) => {
    console.log('\nüîç HOTEL BOOKING PLATFORM FUNCTIONALITY ASSESSMENT');
    console.log('='.repeat(80));

    const results = {
      working: [] as string[],
      broken: [] as string[],
      missing: [] as string[],
      errors: [] as string[],
      consoleErrors: [] as string[],
    };

    // Capture console errors
    page.on('console', msg => {
      if (msg.type() === 'error') {
        results.consoleErrors.push(msg.text());
      }
    });

    // Capture network errors
    page.on('response', response => {
      if (response.status() >= 400) {
        results.errors.push(`HTTP ${response.status()} on ${response.url()}`);
      }
    });

    try {
      // 1. TEST PAGE LOADING
      console.log('\nüåê Testing Page Loading...');
      const title = await page.title();
      if (title && title !== '') {
        results.working.push('‚úÖ Page loads with proper title: ' + title);
        console.log('‚úÖ Page loaded successfully:', title);
      } else {
        results.broken.push('‚ùå Page title missing or empty');
      }

      // Check for main content
      const mainContent = await page.locator('main, #root, .app').first();
      if (await mainContent.isVisible()) {
        results.working.push('‚úÖ Main application content rendered');
        console.log('‚úÖ Main application content found');
      } else {
        results.broken.push('‚ùå Main application content not rendered');
      }

      // 2. TEST AUTHENTICATION UI
      console.log('\nüîê Testing Authentication System...');
      
      // Look for sign-in elements with multiple selectors
      const authSelectors = [
        'button:has-text("Sign In")',
        'button:has-text("Login")', 
        'a:has-text("Sign In")',
        'a:has-text("Login")',
        '[data-testid="sign-in"]',
        '.sign-in-button',
        '.login-button'
      ];

      let authFound = false;
      for (const selector of authSelectors) {
        if (await page.locator(selector).isVisible({ timeout: 1000 })) {
          results.working.push('‚úÖ Authentication button found: ' + selector);
          console.log('‚úÖ Authentication UI element found:', selector);
          authFound = true;
          
          // Test clicking the auth button
          try {
            await page.locator(selector).click();
            await page.waitForTimeout(1000);
            
            // Check if auth modal/form appears
            const authModal = await page.locator('[data-testid="auth-modal"], .modal, [role="dialog"], form').first();
            if (await authModal.isVisible()) {
              results.working.push('‚úÖ Authentication modal/form appears on click');
              console.log('‚úÖ Authentication modal triggered');
            }
          } catch (e) {
            console.log('‚ö†Ô∏è  Auth button click failed:', e);
          }
          break;
        }
      }
      
      if (!authFound) {
        results.missing.push('‚ùå No authentication buttons found');
        console.log('‚ùå No authentication UI elements found');
      }

      // 3. TEST SEARCH FUNCTIONALITY
      console.log('\nüîç Testing Search Functionality...');
      
      // Navigate back to home if needed
      await page.goto('http://localhost:3013');
      await page.waitForLoadState('networkidle');

      // Look for search input with multiple selectors
      const searchSelectors = [
        'input[placeholder*="destination" i]',
        'input[placeholder*="search" i]',
        'input[name="destination"]',
        'input[name="search"]',
        '[data-testid="search-input"]',
        '.search-input'
      ];

      let searchFound = false;
      for (const selector of searchSelectors) {
        const searchInput = page.locator(selector).first();
        if (await searchInput.isVisible({ timeout: 2000 })) {
          results.working.push('‚úÖ Search input found: ' + selector);
          console.log('‚úÖ Search input found:', selector);
          searchFound = true;

          // Test search functionality
          try {
            await searchInput.fill('New York');
            await page.waitForTimeout(500);

            // Look for search trigger (button or Enter key)
            const searchButton = page.locator('button:has-text("Search"), button[type="submit"], .search-button').first();
            if (await searchButton.isVisible({ timeout: 2000 })) {
              await searchButton.click();
            } else {
              await searchInput.press('Enter');
            }

            await page.waitForTimeout(3000);

            // Check for search results
            const resultSelectors = [
              '[data-testid="search-results"]',
              '.hotel-results',
              '.search-results', 
              '.hotel-card',
              '.hotel-item'
            ];

            let resultsFound = false;
            for (const resultSelector of resultSelectors) {
              const results_elements = page.locator(resultSelector);
              const count = await results_elements.count();
              if (count > 0) {
                results.working.push(`‚úÖ Search results displayed: ${count} items found with ${resultSelector}`);
                console.log(`‚úÖ Search results: ${count} items found`);
                resultsFound = true;
                break;
              }
            }

            if (!resultsFound) {
              results.broken.push('‚ùå Search does not return visible results');
              console.log('‚ùå Search executed but no results displayed');
            }

          } catch (e) {
            results.broken.push('‚ùå Search functionality failed: ' + e);
            console.log('‚ùå Search test failed:', e);
          }
          break;
        }
      }

      if (!searchFound) {
        results.missing.push('‚ùå No search input field found');
        console.log('‚ùå Search input not found');
      }

      // 4. TEST HOTEL LISTINGS
      console.log('\nüè® Testing Hotel Listings...');
      
      const hotelCardSelectors = [
        '.hotel-card',
        '.hotel-item',
        '[data-testid="hotel-card"]',
        '.hotel-result',
        '.property-card'
      ];

      let hotelsFound = false;
      for (const selector of hotelCardSelectors) {
        const hotelCards = page.locator(selector);
        const count = await hotelCards.count();
        if (count > 0) {
          results.working.push(`‚úÖ Hotel listings displayed: ${count} hotels found with ${selector}`);
          console.log(`‚úÖ Hotel listings: ${count} hotels displayed`);
          hotelsFound = true;

          // Test hotel images
          const hotelImages = page.locator(`${selector} img`);
          const imageCount = await hotelImages.count();
          if (imageCount > 0) {
            // Check first few images for actual content
            let workingImages = 0;
            for (let i = 0; i < Math.min(imageCount, 3); i++) {
              const img = hotelImages.nth(i);
              const src = await img.getAttribute('src');
              if (src && src.startsWith('http') && !src.includes('placeholder')) {
                workingImages++;
              }
            }
            results.working.push(`‚úÖ Hotel images: ${workingImages}/${imageCount} images appear to be real URLs`);
            console.log(`‚úÖ Hotel images: ${workingImages}/${imageCount} working`);
          } else {
            results.broken.push('‚ùå Hotel listings have no images');
          }
          break;
        }
      }

      if (!hotelsFound) {
        results.missing.push('‚ùå No hotel listings found on page');
        console.log('‚ùå No hotel listings found');
      }

      // 5. TEST BOOKING FUNCTIONALITY
      console.log('\nüìÖ Testing Booking Functionality...');
      
      const bookingSelectors = [
        'button:has-text("Book")',
        'button:has-text("Reserve")', 
        '.book-now-button',
        '.book-button',
        '[data-testid="book-button"]'
      ];

      let bookingFound = false;
      for (const selector of bookingSelectors) {
        const bookButtons = page.locator(selector);
        const count = await bookButtons.count();
        if (count > 0) {
          results.working.push(`‚úÖ Booking buttons found: ${count} buttons with ${selector}`);
          console.log(`‚úÖ Booking buttons: ${count} found`);
          bookingFound = true;

          // Test clicking first booking button
          try {
            await bookButtons.first().click();
            await page.waitForTimeout(2000);

            // Check if booking flow starts
            const bookingFlowSelectors = [
              '[data-testid="booking-form"]',
              '.booking-form',
              '.booking-flow',
              'form:has-text("booking" i)',
              '.checkout-form'
            ];

            let bookingFlowFound = false;
            for (const flowSelector of bookingFlowSelectors) {
              if (await page.locator(flowSelector).isVisible({ timeout: 3000 })) {
                results.working.push('‚úÖ Booking flow initiated successfully');
                console.log('‚úÖ Booking flow started');
                bookingFlowFound = true;
                break;
              }
            }

            if (!bookingFlowFound) {
              results.broken.push('‚ùå Booking button does not start booking flow');
              console.log('‚ùå Booking flow not triggered');
            }

          } catch (e) {
            results.broken.push('‚ùå Booking button click failed: ' + e);
          }
          break;
        }
      }

      if (!bookingFound) {
        results.missing.push('‚ùå No booking buttons found');
        console.log('‚ùå No booking buttons found');
      }

      // 6. TEST PAYMENT INTEGRATION
      console.log('\nüí≥ Testing Payment Integration...');
      
      const paymentSelectors = [
        '[data-testid="payment"]',
        '.payment-form',
        '.payment-section',
        '#sq-card-number',
        '.sq-card-iframe',
        '.paypal-button'
      ];

      let paymentFound = false;
      for (const selector of paymentSelectors) {
        if (await page.locator(selector).isVisible({ timeout: 2000 })) {
          results.working.push(`‚úÖ Payment integration found: ${selector}`);
          console.log(`‚úÖ Payment element found: ${selector}`);
          paymentFound = true;
        }
      }

      if (!paymentFound) {
        results.missing.push('‚ùå No payment integration found');
        console.log('‚ùå No payment elements found');
      }

      // 7. TEST RESPONSIVE DESIGN
      console.log('\nüì± Testing Responsive Design...');
      
      // Test mobile viewport
      await page.setViewportSize({ width: 375, height: 667 });
      await page.waitForTimeout(1000);

      const mobileElements = [
        '.mobile-menu',
        '.hamburger',
        '[data-testid="mobile-menu"]',
        '.nav-toggle'
      ];

      let mobileFound = false;
      for (const selector of mobileElements) {
        if (await page.locator(selector).isVisible({ timeout: 2000 })) {
          results.working.push(`‚úÖ Mobile responsive elements found: ${selector}`);
          console.log(`‚úÖ Mobile design: ${selector} found`);
          mobileFound = true;
        }
      }

      if (!mobileFound) {
        results.broken.push('‚ùå No mobile responsive design elements detected');
      }

      // Reset viewport
      await page.setViewportSize({ width: 1920, height: 1080 });

      // 8. TEST BACKEND API CONNECTION
      console.log('\nüîå Testing Backend API...');
      
      try {
        const apiResponse = await page.request.post('http://localhost:3001/api/hotels/search', {
          data: {
            destination: 'Test City',
            checkIn: '2024-09-01',
            checkOut: '2024-09-05',
            adults: 2
          },
          headers: {
            'Content-Type': 'application/json'
          }
        });

        if (apiResponse.ok()) {
          const data = await apiResponse.json();
          if (data && data.hotels) {
            results.working.push(`‚úÖ Backend API working: returned ${data.hotels.length} hotels`);
            console.log(`‚úÖ Backend API: ${data.hotels.length} hotels returned`);
          } else {
            results.working.push('‚úÖ Backend API responding but with different format');
          }
        } else {
          results.broken.push(`‚ùå Backend API error: HTTP ${apiResponse.status()}`);
        }
      } catch (e) {
        results.broken.push('‚ùå Backend API connection failed: ' + e);
      }

    } catch (error) {
      results.errors.push('Test execution error: ' + error);
      console.error('‚ùå Test execution error:', error);
    }

    // GENERATE FINAL REPORT
    console.log('\n' + '='.repeat(80));
    console.log('üìä COMPREHENSIVE FUNCTIONALITY TEST REPORT');
    console.log('='.repeat(80));
    
    console.log('\n‚úÖ WORKING FEATURES (' + results.working.length + '):');
    results.working.forEach(item => console.log('  ' + item));
    
    console.log('\n‚ùå BROKEN/NON-FUNCTIONAL (' + results.broken.length + '):');
    results.broken.forEach(item => console.log('  ' + item));
    
    console.log('\nüö´ MISSING FEATURES (' + results.missing.length + '):');
    results.missing.forEach(item => console.log('  ' + item));
    
    if (results.errors.length > 0) {
      console.log('\nüö® TECHNICAL ERRORS (' + results.errors.length + '):');
      results.errors.forEach(item => console.log('  ' + item));
    }
    
    if (results.consoleErrors.length > 0) {
      console.log('\n‚ö†Ô∏è  CONSOLE ERRORS (' + results.consoleErrors.length + '):');
      results.consoleErrors.slice(0, 5).forEach(item => console.log('  ' + item));
      if (results.consoleErrors.length > 5) {
        console.log('  ... and ' + (results.consoleErrors.length - 5) + ' more');
      }
    }

    // PRIORITIZED FIXES
    console.log('\nüîß PRIORITIZED FIXES NEEDED:');
    
    const priorityFixes = [];
    
    if (results.broken.some(item => item.includes('Search does not return'))) {
      priorityFixes.push('üî¥ CRITICAL: Fix search functionality - users cannot find hotels');
    }
    if (results.missing.includes('‚ùå No hotel listings found on page')) {
      priorityFixes.push('üî¥ CRITICAL: Hotel listings not displaying - core functionality broken');
    }
    if (results.broken.some(item => item.includes('Backend API'))) {
      priorityFixes.push('üî¥ CRITICAL: Backend API connectivity issues');
    }
    if (results.missing.includes('‚ùå No authentication buttons found')) {
      priorityFixes.push('üü° HIGH: Authentication system missing - users cannot sign in');
    }
    if (results.missing.includes('‚ùå No booking buttons found')) {
      priorityFixes.push('üü° HIGH: Booking functionality missing - cannot complete reservations');
    }
    if (results.missing.includes('‚ùå No payment integration found')) {
      priorityFixes.push('üü° HIGH: Payment integration missing - cannot process payments');
    }
    if (results.broken.some(item => item.includes('mobile responsive'))) {
      priorityFixes.push('üü† MEDIUM: Mobile responsive design needs improvement');
    }

    if (priorityFixes.length === 0) {
      console.log('  üéâ No critical issues found! Platform appears to be functioning well.');
    } else {
      priorityFixes.forEach(fix => console.log('  ' + fix));
    }

    console.log('\n' + '='.repeat(80));
    console.log('‚ú® Assessment completed at: http://localhost:3013');
    console.log('üîó Backend running at: http://localhost:3001');
    console.log('='.repeat(80));

    // Test passes if basic functionality works
    const criticalIssues = results.broken.filter(item => 
      item.includes('Page title missing') ||
      item.includes('Main application content not rendered') ||
      item.includes('Backend API connection failed')
    );

    if (criticalIssues.length > 0) {
      throw new Error('Critical functionality issues found: ' + criticalIssues.join(', '));
    }
  });
});